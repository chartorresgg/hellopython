"""
This module demonstrates the use of basic numeric types in Python.
"""
number = 10 # Integer
decimal = 3.14 # Float
complex = 2 + 3j # Complex number
imaginary = 2 + 5j # Imaginary number

# Operations with numbers
number = number + 15  # Addition. Replace the value of number with the result of the addition.
difference_result = decimal - 1.14  # Subtraction
product_result = number * 2  # Multiplication
division_result = decimal / 2  # Division
exponent_result = number ** 2  # Exponentiation
floor_division_result = number // 3  # Floor Division: quotient of division without remainder.
modulus_result = number % 3  # Modulus: remainder of division.

print("Basic Numeric Types in Python:")
print("Integer:", number)
print("Float:", decimal)
print("Complex Number:", complex)
print("Imaginary Number:", imaginary)
print("Addition operation:", number)
print("Substraction opperation:", difference_result)
print("Multiplication operation:", product_result)
print("Division operation:", division_result)
print("Exponentiation operation:", exponent_result)
print("Floor Division operation:", floor_division_result)
print("Modulus operation:", modulus_result)
