"""
Escape sequences in Python are used to represent special characters within strings.
"""
# Escape Squences in Python
# \ - Backslash
# \" - Double quote
# \' - Single quote
# \n" - New line
# \t - Tab
# \r - Carriage return
# Example of escape sequences

NAME = "Print: Thommas Shelby"
print(NAME)


NAME_1 = "New line: Thommas \nShelby"
print(NAME_1)

NAME_2 = "Tab: Thommas \tShelby"
print(NAME_2)

NAME_3 = "Backslash: Thommas \\ Shelby"
print(NAME_3)

NAME_4 = "Single quote: Thommas 'Shelby'"
print(NAME_4)

NAME_5 = "Double quote: Thommas \"Shelby\""
print(NAME_5)

NAME_6 = "Carriage return: Thommas \rShelby"
print(NAME_6)

NAME_7 = "Backspace: Thommas \bShelby"
print(NAME_7)

NAME_8 = "Form feed: Thommas \fShelby"
print(NAME_8)

NAME_9 = "Octal value: Thommas \101Shelby"
print(NAME_9)

NAME_10 = "Simple quote in a String: I\'m Thommas Shelby"
print(NAME_10)
